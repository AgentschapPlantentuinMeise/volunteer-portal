/**
 * 
 * @param notebookId
 * @param offset - either 1 or -1
 */
function showNotebookPage(notebookId, offset) {
    var notebookUri = null;
    var notebookImage = notebookId + ".jpg";
    var notebookPrefixUri = "http://volunteer.ala.org.au/uploads/scottSisters/NoteBookPages/";

    var currentIndex = $.inArray(notebookImage, notebookPages);

    if (currentIndex > 0) {
        if (offset > 0 && notebookPages[currentIndex + 1]) {
            notebookUri = notebookPrefixUri + notebookPages[currentIndex + 1];
        } else if (offset < 0 && notebookPages[currentIndex - 1]) {
            notebookUri = notebookPrefixUri + notebookPages[currentIndex - 1];
        }
    }

    return notebookUri;
}